#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int DX[4] = {1, 0, -1, 0};
const int DY[4] = {0, 1, 0, -1};

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<vector<int>> wochien(n, vector<int>(n, -1));
        int cx = 0; int cy = 0; int dir = 0;
        for(int i = n * n - 1; i >= 0; i--) {
            wochien[cx][cy] = i;
            if(cx + DX[dir] >= 0 && cx + DX[dir] < n && cy + DY[dir] >= 0 && cy + DY[dir] < n && wochien[cx + DX[dir]][cy + DY[dir]] == -1) {
                cx += DX[dir];
                cy += DY[dir];
            } else {
                dir = (dir + 1) % 4;
                cx += DX[dir];
                cy += DY[dir];
            }
        }
        for(vector<int> i : wochien) {
            for(int j : i) {
                cout << j << " ";
            }
            cout << "\n";
        }
    }
}
