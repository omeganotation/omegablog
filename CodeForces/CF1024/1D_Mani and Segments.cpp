#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i]; a[i]--;
        }
        vector<int> pos(n);
        for(int i = 0; i < n; i++) {
            pos[a[i]] = i;
        }
        set<int> ls; set<int> rb;
        set<int> lb; set<int> rs; 
        ls.insert(-1); rb.insert(n);
        lb.insert(-1); rs.insert(n);
        set<int> bigger; set<int> smaller;
        for(int i = 0; i + 1 < n; i++) {
            if(a[i] > a[i + 1]) {
                rb.insert(i + 1);
            } else {
                lb.insert(i);
            }
            bigger.insert(i);
        }
        bigger.insert(n - 1);
        ll wochien = 0;
        vector<int> right(n, -1);
        for(int i = 0; i < n; i++) {
            int p = pos[i];
            rb.erase(p); lb.erase(p);
            smaller.insert(p);
            set<int>::iterator it = smaller.find(p);
            if(it != smaller.begin() && next(it) != smaller.end()) {
                if(a[*next(it)] > a[*prev(it)]) {
                    rs.erase(*next(it));
                } else {
                    ls.erase(*prev(it));
                }
            }
            int l = max(*prev(ls.lower_bound(p)), *prev(lb.lower_bound(p))) + 1;
            int r = min(*rb.upper_bound(p), *rs.upper_bound(p)) - 1;
            right[l] = max(right[l], r);
            if(it != smaller.begin()) {
                rs.insert(p); 
            } 
            if(next(it) != smaller.end()) {
                ls.insert(p);
            }
            it = bigger.find(p);
            if(it != bigger.begin() && next(it) != bigger.end()) {
                if(a[*next(it)] > a[*prev(it)]) {
                    lb.insert(*prev(it));
                } else {
                    rb.insert(*next(it));
                }
            }
            bigger.erase(p);
        }
        int maxR = -1;
        for(int i = 0; i < n; i++) {
            maxR = max(maxR, right[i]);
            wochien += maxR - i + 1;
        }
        cout << wochien << "\n";
    }
}
