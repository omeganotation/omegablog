#include <bits/stdc++.h>
#include <ext/pb_ds/assoc_container.hpp>
#include <ext/pb_ds/tree_policy.hpp>

using namespace std;
using namespace __gnu_pbds;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

using indexed_set = tree<int, null_type, less<int>, rb_tree_tag, tree_order_statistics_node_update>;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        indexed_set st; indexed_set st2;
        vector<int> pos(n + 1);
        vector<int> even; vector<int> odd;
        for(int i = 0; i < n; i++) {
            pos[a[i]] = i;
            if(i % 2) {
                odd.push_back(a[i]);
                st2.insert(i);
            } else {
                even.push_back(a[i]);
                st.insert(i);
            }
        }
        sort(odd.begin(), odd.end());
        sort(even.begin(), even.end());
        vector<int> wochien(n);
        for(int i = 0; i + 3 < n; i++) {
            if(!(i % 2)) {
                int v = st.order_of_key(pos[even[i / 2]]);
                if(v % 2) {
                    int x = *st2.begin();
                    int y = *next(st2.begin());
                    pos[a[x]] = y; pos[a[y]] = x;
                    swap(a[x], a[y]);
                }
                st.erase(pos[even[i / 2]]);
                wochien[i] = even[i / 2];
            } else {
                int v = st2.order_of_key(pos[odd[i / 2]]);
                if(v % 2) {
                    int x = *st.begin();
                    int y = *next(st.begin());
                    pos[a[x]] = y; pos[a[y]] = x;
                    swap(a[x], a[y]);
                }
                st2.erase(pos[odd[i / 2]]);
                wochien[i] = odd[i / 2];
            }
        }
        if(st.size() > 1) {
            wochien[n - 3] = a[*st.begin()];
            wochien[n - 2] = a[*st2.begin()];
            wochien[n - 1] = a[*next(st.begin())];
        } else {
            wochien[n - 3] = a[*st2.begin()];
            wochien[n - 2] = a[*st.begin()];
            wochien[n - 1] = a[*next(st2.begin())];
        }
        for(int i : wochien) {
            cout << i << " ";
        }
        cout << "\n";
    }
}
