#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n; int k;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> k;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        vector<ll> pref(n);
        for(int i = 0; i + 1 < n; i++) {
            int p2 = __builtin_ctz(a[i]);
            int v = a[i] / (1 << p2);
            if(!(a[i + 1] % v)) {
                if(a[i + 1] <= a[i] && __builtin_popcount(a[i + 1] / v) == 1) {
                    pref[i] = 1 + (a[i] - 2 * a[i + 1]) / v;
                } else {
                    pref[i] = (1 << p2);
                }
            } else {
                pref[i] = (1 << p2);
            }
            if(i) {
                pref[i] += pref[i - 1];
            }
        }
        vector<ll> suff(n);
        for(int i = n - 1; i; i--) {
            int p2 = __builtin_ctz(a[i]);
            int v = a[i] / (1 << p2);
            if(!(a[i - 1] % v)) {
                if(a[i - 1] <= a[i] && __builtin_popcount(a[i - 1] / v) == 1) {
                    suff[i] = 1 + (a[i] - 2 * a[i - 1]) / v;
                } else {
                    suff[i] = (1 << p2);
                }
            } else {
                suff[i] = (1 << p2);
            }
            if(i + 1 < n) {
                suff[i] += suff[i + 1];
            }
        }
        bool wochien = false;
        for(int i = 0; i < n; i++) {
            int p2 = __builtin_ctz(a[i]);
            ll cur = (1 << p2);
            if(i) {
                cur += pref[i - 1];
            }
            if(i + 1 < n) {
                cur += suff[i + 1];
            }
            if(cur >= k) {
                wochien = true;
            }
        }
        cout << (wochien ? "YES" : "NO") << "\n";
    }
}
