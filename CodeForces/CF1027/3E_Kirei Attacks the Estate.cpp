#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int N = 2E5;
array<ll, 2> dp[N];
ll wochien[N];

void dfs(int x, vector<vector<int>>& adj, vector<int>& a, bool neg, int p) {
    if(neg) {
        dp[x][0] = min(dp[x][0], 1LL * -a[x]);
        dp[x][1] = max(dp[x][1], 1LL * -a[x]);
    } else {
        dp[x][0] = min(dp[x][0], 1LL * a[x]);
        dp[x][1] = max(dp[x][1], 1LL * a[x]);
    }
    wochien[x] = (neg ? -dp[x][0] : dp[x][1]);
    for(int i : adj[x]) {
        if(i == p) {
            continue;
        }
        if(neg) {
            dp[i][0] = dp[x][0] + a[i];
            dp[i][1] = dp[x][1] + a[i];
        } else {
            dp[i][0] = dp[x][0] - a[i];
            dp[i][1] = dp[x][1] - a[i];
        }
        dfs(i, adj, a, !neg, x);
    }
}

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        vector<vector<int>> adj(n);
        for(int i = 0; i + 1 < n; i++) {
            int u; int v;
            cin >> u >> v;
            u--; v--;
            adj[u].push_back(v);
            adj[v].push_back(u);
        }
        dp[0] = {a[0], a[0]};
        dfs(0, adj, a, false, -1);
        for(int i = 0; i < n; i++) {
            cout << wochien[i] << " ";
        }
        cout << "\n";
    }
}
