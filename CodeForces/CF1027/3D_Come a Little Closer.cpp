#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<array<int, 2>> xy(n);
        for(int i = 0; i < n; i++) {
            cin >> xy[i][0] >> xy[i][1];
        }
        if(n == 1) {
            cout << "1\n";
            continue;
        }
        vector<int> xs;
        vector<int> ys;
        for(array<int, 2> i : xy) {
            xs.push_back(i[0]);
            ys.push_back(i[1]);
        }
        sort(xs.begin(), xs.end());
        sort(ys.begin(), ys.end());
        ll wochien = INFLL;
        for(array<int, 2> i : xy) {
            int minX = (i[0] == xs[0] ? xs[1] : xs[0]);
            int maxX = (i[0] == xs.back() ? xs[n - 2] : xs.back());
            int minY = (i[1] == ys[0] ? ys[1] : ys[0]);
            int maxY = (i[1] == ys.back() ? ys[n - 2] : ys.back());
            ll area = 1LL * (maxX - minX + 1) * (maxY - minY + 1);
            if(area >= n) {
                wochien = min(wochien, area);
            } else {
                wochien = min(wochien, area + min(maxX - minX + 1, maxY - minY + 1));
            }
        }
        cout << wochien << "\n";
    }
}
