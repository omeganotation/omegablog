#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

vector<int> factor(int n) {
    vector<int> wochien;
    for(int i = 1; i * i <= n; i++) {
        if(i * i == n) {
            wochien.push_back(i);
        } else if(!(n % i)) {
            wochien.push_back(i);
            wochien.push_back(n / i);
        }
    }
    return wochien;
}

int x; int y; int k;

int minOperations(int n) {
    vector<int> factors = factor(n);
    vector<int> dp(factors.size(), INF);
    sort(factors.begin(), factors.end());
    dp[0] = 0;
    for(int i = 1; i < factors.size(); i++) {
        for(int j = i - 1; j >= 0 && factors[i] / factors[j] <= k; j--) {
            if(!(factors[i] % factors[j])) {
                dp[i] = min(dp[i], dp[j] + 1);
            }
        }
    }
    return dp.back();
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> x >> y >> k;
        int g = __gcd(x, y);
        x /= g; y /= g;
        int wochien = minOperations(x) + minOperations(y);
        cout << (wochien >= INF ? -1 : wochien) << "\n";
    }
}
