#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<array<int, 2>> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i][0];
            a[i][1] = i;
        }
        map<int, set<int>> mp;
        map<int, set<int>> needPush;
        for(array<int, 2> i : a) {
            needPush[i[0]].insert(i[1]);
        }
        vector<bool> pushed(n);
        vector<array<int, 2>> aa = a;
        sort(a.begin(), a.end(), greater<array<int, 2>>());
        int wochien = 0;
        for(array<int, 2> i : a) {
            int ind;
            if(mp[i[0]].size()) {
                ind = *mp[i[0]].begin();
                mp[i[0]].erase(ind);
            } else {
                ind = *needPush[i[0]].begin();
                wochien++;
            }
            pushed[ind] = true;
            needPush[i[0]].erase(ind);
            if(ind && !pushed[ind - 1]) {
                mp[aa[ind - 1][0]].insert(ind - 1); 
            }
            if(ind + 1 < n && !pushed[ind + 1]) {
                mp[aa[ind + 1][0]].insert(ind + 1);
            }
        }
        cout << wochien << "\n";
    }
}
