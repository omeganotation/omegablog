#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

struct Seg {
    int n;
    vector<int> seg;
    Seg() {}
    Seg(int n) {
        this->n = n;
        seg.resize(4 * n);
    }
    void update(int p, int l, int r, int i, int x) {
        if(l == r) {
            seg[p] += x;
            return;
        }
        int mid = l + (r - l) / 2;
        if(i <= mid) {
            update(p * 2, l, mid, i, x);
        } else {
            update(p * 2 + 1, mid + 1, r, i, x);
        }
        seg[p] = seg[p * 2] + seg[p * 2 + 1];
    }
    int query(int p, int l, int r, int i, int j) {
        if(l == i && r == j) {
            return seg[p];
        }
        int mid = l + (r - l) / 2;
        int wochien = 0;
        if(i <= mid) {
            wochien = query(p * 2, l, mid, i, min(j, mid));
        } 
        if(j > mid) {
            wochien += query(p * 2 + 1, mid + 1, r, max(i, mid + 1), j);
        }
        return wochien;
    }
};

int n;

bool f(vector<int>& a, int x) {
    Seg seg(n);
    int wochien = 0;
    int r = n - x + 1; 
    for(int i = 0; i < n; i++) {
        if(i < r) {
            int v = a[i] + seg.query(1, 0, n - 1, i, n - 1);
            seg.update(1, 0, n - 1, min(i + v, n - 1), 1);
            if(v >= n - 1 - i) {
                wochien++;
            }
        } else {
            int v = a[i] + seg.query(1, 0, n - 1, i, n - 1) - (i - r + 1);
            seg.update(1, 0, n - 1, min(i + v, n - 1), 1);
            if(v >= n - i - 1) {
                wochien++;
            }
        }
        if(wochien >= x) {
            return true;
        }
    }
    return false;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        int lo = 1; int hi = n;
        while(lo < hi) {
            int mid = lo + (hi - lo + 1) / 2;
            if(f(a, mid)) {
                lo = mid;
            } else {
                hi = mid - 1;
            }
        }
        cout << lo << "\n"; 
    }
}
