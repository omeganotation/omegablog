#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int query(int x) {
    cout << "? " << x + 1 << endl;
    int res; cin >> res;
    return res;
}

int n; int k;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> k;
        if(n == k + k) {
            cout << "! " << k << " " << k << endl;
            continue;
        }
        vector<int> a(k);
        vector<int> b(k);
        for(int i = 0; i < k; i++) {
            a[i] = query(i);
            b[(n - 1 - i) % k] = query(n - 1 - i);
        }
        if(a == b) {
            cout << "! -1" << endl;
            continue;
        }
        int lo; int hi; int mod;
        for(int i = 0; i < k; i++) {
            if(a[i] != b[i]) {
                mod = i;
                lo = 0;
                hi = n / k + (n % k > i);
                break;
            }
        }
        while(lo < hi) {
            int mid = lo + (hi - lo) / 2;
            int pos = mid * k + mod;
            if(query(pos) == a[mod]) {
                lo = mid + 1;
            } else {
                hi = mid;
            }
        }
        int split = lo * k + mod;
        while(split > k && query(split - 1) == b[(split - 1 + k) % k]) {
            split--;
        }
        if(split < n - k && query(split) == a[split % k]) {
            cout << "! -1" << endl;
            continue;
        }
        cout << "! " << split << " " << n - split << endl;
    }
}
