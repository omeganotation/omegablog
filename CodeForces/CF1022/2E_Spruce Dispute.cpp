#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int MAX = 2E5;
int subtreeSize[MAX];
int subtree[MAX];
int depth[MAX];
ll val[MAX];
ll cur = 0;
int cnt;

int n;

int centroid;
ll best; array<int, 2> wochien;

void dfs(int x, vector<vector<int>>& adj, int p) {
    subtreeSize[x] = 1;
    for(int i : adj[x]) {
        if(i != p) {
            dfs(i, adj, x);
            subtreeSize[x] += subtreeSize[i];
        }
    }
}

int dfs2(int x, vector<vector<int>>& adj, int p) {
    for(int i : adj[x]) {
        if(i != p && subtreeSize[i] > n / 2) {
            return dfs2(i, adj, x);
        }
    }
    return x;
}

void dfs3(int x, vector<vector<int>>& adj, int p) {
    subtreeSize[x] = 1;
    val[x] = 0;
    for(int i : adj[x]) {
        if(i != p) {
            depth[i] = depth[x] + 1;
            subtree[i] = subtree[x];
            dfs3(i, adj, x);
            subtreeSize[x] += subtreeSize[i];
            val[x] += val[i];
        }
    }
    cur += subtreeSize[x];
    val[x] += subtreeSize[x];
}

void dfs4(int x, vector<vector<int>>& adj, int p) {
    for(int i : adj[x]) {
        if(i != p) {
            ll cur2 = cur - subtreeSize[i] - (depth[i] - 1);
            if(cur2 > best) {
                best = cur2;
                wochien = {x, i};
            }
            dfs4(i, adj, x);
        }
    }
}

void dfs5(int x, vector<vector<int>>& adj, vector<int>& c, int p) {
    if(x != wochien[1]) {
        c[x] = (cnt++) % (n / 2);
    }
    for(int i : adj[x]) {
        if(i != p) {
            dfs5(i, adj, c, x);
        }
    }
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        best = 0;
        cin >> n;
        vector<vector<int>> adj(n);
        for(int i = 0; i + 1 < n; i++) {
            int u; int v;
            cin >> u >> v;
            u--; v--;
            adj[u].push_back(v);
            adj[v].push_back(u);
        }
        cur = 0;
        dfs(0, adj, -1);
        centroid = dfs2(0, adj, -1);
        depth[centroid] = 0;
        for(int i : adj[centroid]) {
            subtree[i] = i;
            depth[i] = 1;
            dfs3(i, adj, centroid);
        }
        dfs4(centroid, adj, -1);
        if(wochien[1] == centroid) {
            swap(wochien[0], wochien[1]);
        }
        vector<int> c(n, -1);
        cnt = 0;
        dfs5(centroid, adj, c, -1);
        if(wochien[1] < wochien[0]) {
            swap(c[wochien[0]], c[wochien[1]]);
            swap(wochien[0], wochien[1]);
        }
        cout << wochien[0] + 1 << " " << wochien[1] + 1 << "\n";
        for(int i : c) {
            cout << i + 1 << " ";
        }
        cout << "\n"; 
    }
}
