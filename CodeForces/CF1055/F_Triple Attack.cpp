#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int MAX = 2.5E5 + 10;
const int LOG = 18;
int lift[MAX][LOG];
array<int, 2> lift2[MAX][LOG];

int n; int z; int q;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> z;
        vector<int> x(n);
        for(int i = 0; i < n; i++) {
            cin >> x[i];
        }
        cin >> q;
        for(int i = 0; i < n; i++) {
            vector<int>::iterator it = upper_bound(x.begin(), x.end(), x[i] + z);
            int ind = it - x.begin();
            lift[i][0] = ind;
        }
        lift[n][0] = n;
        for(int i = 1; i < LOG; i++) {
            for(int j = 0; j <= n; j++) {
                lift[j][i] = lift[lift[j][i - 1]][i - 1];
            }
        }
        for(int i = n - 2; i >= 0; i--) {
            if(x[i] + z < x[i + 1]) {
                lift2[i][0] = {i + 1, 1};
                continue;
            }
            int dist = 2;
            int a = i; int b = i + 1;
            for(int j = LOG - 1; j >= 0; j--) {
                if(lift[a][j] != lift[b][j]) {
                    a = lift[a][j];
                    b = lift[b][j];
                    dist += (1 << (j + 1));
                }
            }
            if(b == n) {
                dist--;
            }
            int dist2 = INF;
            int va = lift[a][0]; int vb = -1;
            if(lift[i][0] < n) {
                dist2 = 3;
                a = i + 1; b = lift[i][0];
                for(int j = LOG - 1; j >= 0; j--) {
                    if(lift[a][j] != lift[b][j]) {
                        a = lift[a][j];
                        b = lift[b][j];
                        dist2 += (1 << (j + 1));
                    }
                }
                if(b == n) {
                    dist2--;
                }
                vb = lift[a][0];
            }
            if(dist < dist2) {
                lift2[i][0] = {va, dist};
            } else {
                lift2[i][0] = {vb, dist2};
            }
        }
        lift2[n - 1][0] = {n, 1};
        lift2[n][0] = {n, 0};
        for(int i = 1; i < LOG; i++) {
            for(int j = 0; j <= n; j++) {
                lift2[j][i] = {lift2[lift2[j][i - 1][0]][i - 1][0], lift2[j][i - 1][1] + lift2[lift2[j][i - 1][0]][i - 1][1]};
            }
        }
        for(int i = 0; i < q; i++) {
            int l; int r;
            cin >> l >> r;
            l--; r--;
            int wochien = 0;
            for(int j = LOG - 1; j >= 0; j--) {
                if(lift2[l][j][0] <= r) {
                    wochien += lift2[l][j][1];
                    l = lift2[l][j][0];
                }
            }
            if(l == r) {
                cout << wochien + 1 << "\n";
            } else {
                int dist = 2;
                int a = l; int b = l + 1;
                for(int j = LOG - 1; j >= 0; j--) {
                    if(lift[a][j] <= r) {
                        wochien += (1 << j);
                        a = lift[a][j];
                    }
                    if(lift[b][j] <= r) {
                        wochien += (1 << j);
                        b = lift[b][j];
                    }
                }
                cout << wochien + dist << "\n";
            }
        }
    } 
}
