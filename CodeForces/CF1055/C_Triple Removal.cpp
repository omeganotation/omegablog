#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n; int q;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> q;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        vector<int> pref(n + 1);
        vector<int> pref2(n + 1);
        for(int i = 0; i < n; i++) {
            if(i + 1 < n && a[i + 1] == a[i]) {
                pref2[i + 1]++;
            }
            pref[i + 1] = pref[i] + a[i];
            pref2[i + 1] += pref2[i];
        }
        for(int i = 0; i < q; i++) {
            int l; int r;
            cin >> l >> r;
            l--; r--;
            if((r - l + 1) % 3) {
                cout << "-1\n";
                continue;
            }
            int cnt = pref[r + 1] - pref[l];
            if(cnt % 3) {
                cout << "-1\n";
                continue;
            }
            int cntSame = pref2[r] - pref2[l];
            if(cntSame) {
                cout << (r - l + 1) / 3 << "\n";
            } else {
                cout << (r - l + 1) / 3 + 1 << "\n";
            }
        }
    } 
}
