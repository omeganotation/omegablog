#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int LOG = 30;

int n; int q; 

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> q;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        vector<int> type1(n + 1);
        vector<int> type2(n + 1);
        vector<int> pref(n + 1);
        for(int i = 0; i < n; i++) {
            pref[i + 1] = 31 - __builtin_clz(a[i]);
            if(__builtin_popcount(a[i]) == 1) {
                type1[i + 1] = 1;
            } else if(__builtin_popcount(a[i] / 2) == 1) {
                type2[i + 1] = 1;
            }
            type1[i + 1] += type1[i];
            type2[i + 1] += type2[i];
            pref[i + 1] += pref[i];
        }
        for(int i = 0; i < q; i++) {
            int l; int r; 
            cin >> l >> r;
            l--; r--;
            int c1 = type1[r + 1] - type1[l];
            int c2 = type2[r + 1] - type2[l];
            int c3 = r - l + 1 - (c1 + c2);
            int wochien = pref[r + 1] - pref[l];
            wochien += c3 + c2 / 2;
            cout << wochien << "\n";
        }
    }
}
