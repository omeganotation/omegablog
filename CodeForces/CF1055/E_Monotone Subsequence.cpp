#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

vector<int> query(vector<int>& a) {
    cout << "? " << a.size() << " ";
    for(int i : a) {
        cout << i + 1 << " ";
    }
    cout << endl;
    vector<int> wochien;
    int k; cin >> k;
    wochien.resize(k);
    for(int i = 0; i < k; i++) {
        cin >> wochien[i];
        wochien[i]--;
    }
    return wochien;
}

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<bool> seen(n * n + 1);
        vector<int> greater(n * n + 1, -1);
        vector<int> wochien;
        for(int i = 0; i < n; i++) {
            vector<int> v;
            for(int j = 0; j < n * n + 1; j++) {
                if(!seen[j]) {
                    v.push_back(j);
                }
            }
            vector<int> v2 = query(v);
            if(v2.size() > n) {
                wochien = v2;
                break;
            }
            for(int j = 0; j < v2.size(); j++) {
                seen[v2[j]] = true;
                int nxt = (j + 1 < v2.size() ? v2[j + 1] : n * n + 1);
                for(int k = v2[j] + 1; k < nxt; k++) {
                    if(!seen[k]) {
                        greater[k] = v2[j];
                    }
                }
            }
        }
        if(wochien.size()) {
            cout << "! ";
            for(int i = 0; i <= n; i++) {
                cout << wochien[i] + 1 << " ";
            }
            cout << endl;
        } else {
            int v = find(seen.begin(), seen.end(), false) - seen.begin();
            wochien.resize(n + 1);
            for(int i = n; i >= 0; i--) {
                wochien[i] = v;
                v = greater[v];
            }
            cout << "! ";
            for(int i = 0; i <= n; i++) {
                cout << wochien[i] + 1 << " ";
            }
            cout << endl;
        }
    }
}
