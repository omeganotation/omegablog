#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

struct Seg {
    int n;
    vector<int> seg;
    vector<int> seg2;
    vector<int> lazy;
    Seg() {}
    Seg(vector<int> a) {
        n = a.size();
        seg.resize(4 * n);
        seg2.resize(4 * n);
        lazy.resize(4 * n);
        build(1, 0, n - 1, a);
    }
    void build(int p, int l, int r, vector<int>& a) {
        if(l == r) {
            seg[p] = a[l];
            seg2[p] = a[l];
            return;
        }
        int mid = l + (r - l) / 2;
        build(p * 2, l, mid, a);
        build(p * 2 + 1, mid + 1, r, a);
        seg[p] = max(seg[p * 2], seg[p * 2 + 1]);
        seg2[p] = min(seg2[p * 2], seg2[p * 2 + 1]);
    }
    void prop(int p) {
        if(lazy[p]) {
            seg[p * 2] += lazy[p];
            seg2[p * 2] += lazy[p];
            lazy[p * 2] += lazy[p];
            seg[p * 2 + 1] += lazy[p];
            seg2[p * 2 + 1] += lazy[p];
            lazy[p * 2 + 1] += lazy[p];
            lazy[p] = 0;
        }
    }
    void update(int p, int l, int r, int i, int j, int x) {
        if(l == i && r == j) {
            seg[p] += x;
            seg2[p] += x;
            lazy[p] += x;
            return;
        }
        prop(p);
        int mid = l + (r - l) / 2;
        if(i <= mid) {
            update(p * 2, l, mid, i, min(j, mid), x);
        }
        if(j > mid) {
            update(p * 2 + 1, mid + 1, r, max(i, mid + 1), j, x);
        }
        seg[p] = max(seg[p * 2], seg[p * 2 + 1]);
        seg2[p] = min(seg2[p * 2], seg2[p * 2 + 1]);
    }
    int query(int p, int l, int r, int i, int j) {
        if(l == i && r == j) {
            return seg[p];
        }
        prop(p);
        int mid = l + (r - l) / 2;
        int wochien = -INF;
        if(i <= mid) {
            wochien = query(p * 2, l, mid, i, min(j, mid));
        }
        if(j > mid) {
            wochien = max(wochien, query(p * 2 + 1, mid + 1, r, max(i, mid + 1), j));
        }
        return wochien;
    }
    int query2(int p, int l, int r, int i, int j) {
        if(l == i && r == j) {
            return seg2[p];
        }
        prop(p);
        int mid = l + (r - l) / 2;
        int wochien = INF;
        if(i <= mid) {
            wochien = query2(p * 2, l, mid, i, min(j, mid));
        }
        if(j > mid) {
            wochien = min(wochien, query2(p * 2 + 1, mid + 1, r, max(i, mid + 1), j));
        }
        return wochien;
    }
};

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i]; a[i]--;
        }
        vector<vector<int>> pos(n);
        for(int i = 0; i < n; i++) {
            pos[a[i]].push_back(i);
        }
        vector<int> prv(n, -1);
        vector<int> nxt(n, n);
        stack<int> stk;
        for(int i = 0; i < n; i++) {
            while(stk.size() && a[i] < a[stk.top()]) {
                nxt[stk.top()] = i;
                stk.pop();
            }
            stk.push(i);
        }
        while(stk.size()) {
            stk.pop();
        }
        for(int i = 0; i < n; i++) {
            while(stk.size() && a[i] <= a[stk.top()]) {
                stk.pop();
            }
            if(stk.size()) {
                prv[i] = stk.top();
            }
            stk.push(i);
        }
        vector<array<int, 5>> queries(n);
        for(int i = 0; i < n; i++) {
            queries[i] = {prv[i] + 1, i, nxt[i] - 1, 0, n - 1};
        }
        int wochien = 0;
        while(queries.size()) {
            vector<array<int, 5>> q2(n);
            vector<int> v(n + 1);
            iota(v.begin(), v.end(), 0);
            for(int i = 0; i <= n; i++) {
                v[i] = -v[i];
            }
            Seg seg(v);
            vector<vector<array<int, 5>>> mid(n);
            for(array<int, 5> i : queries) {
                if(i[3] != i[4]) {
                    mid[i[3] + (i[4] - i[3] + 1) / 2].push_back(i);
                }
            }
            int cnt = 0;
            for(int i = n - 1; i >= 0; i--) {
                for(int j : pos[i]) {
                    seg.update(1, 0, n, j + 1, n, 2);
                }
                for(array<int, 5> j : mid[i]) {
                    int mr = seg.query(1, 0, n, j[1] + 1, j[2] + 1);
                    int ml = seg.query2(1, 0, n, j[0], j[1]);
                    int l = j[3]; int r = j[4];
                    if(mr - ml >= 0) {
                        l = i;
                    } else {
                        r = i - 1;
                    }
                    q2[j[1]] = {j[0], j[1], j[2], l, r};
                    if(l == r) {
                        wochien = max(wochien, l - a[j[1]]);
                    } else {
                        cnt++;
                    }
                }
            }
            if(!cnt) {
                break;
            }
            queries = q2;
        }
        cout << wochien << "\n";
    }
}
