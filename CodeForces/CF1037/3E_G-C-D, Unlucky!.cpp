#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

ll lcm(ll a, ll b) {
    return a / __gcd(a, b) * b;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<ll> p(n);
        vector<ll> s(n);
        for(int i = 0; i < n; i++) {
            cin >> p[i];
        }
        for(int i = 0; i < n; i++) {
            cin >> s[i];
        }
        vector<ll> wochien(n);
        for(int i = 0; i < n; i++) {
            wochien[i] = lcm(p[i], s[i]);
        }
        vector<ll> pp(n); vector<ll> ss(n);
        pp[0] = wochien[0];
        for(int i = 1; i < n; i++) {
            pp[i] = __gcd(wochien[i], pp[i - 1]);
        }
        ss[n - 1] = wochien[n - 1];
        for(int i = n - 2; i >= 0; i--) {
            ss[i] = __gcd(wochien[i], ss[i + 1]);
        }
        cout << (pp == p && ss == s ? "YES" : "NO") << "\n";
    }
}
