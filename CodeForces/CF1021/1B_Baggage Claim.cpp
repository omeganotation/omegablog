#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

struct DSU {
    int n;
    vector<int> p;
    vector<int> cntGiven;
    vector<int> cntEdges;
    DSU() {}
    DSU(int n) {
        p.resize(n, -1);
        cntGiven.resize(n);
        cntEdges.resize(n);
    }
    int find(int x) {
        if(p[x] < 0) {
            return x;
        }
        int px = find(p[x]);
        p[x] = px;
        return px;
    }
    void unite(int x, int y) {
        int px = find(x);
        int py = find(y);
        if(px == py) {
            cntEdges[px]++;
            return;
        }
        if(p[px] > p[py]) {
            swap(px, py);
        }
        p[px] += p[py];
        cntGiven[px] += cntGiven[py];
        cntEdges[px] += cntEdges[py] + 1;
        p[py] = px;
    }
    bool sameSet(int x, int y) {
        return find(x) == find(y);
    }
    void setGiven(int x) {
        cntGiven[x] = 1;
    }
    int given(int x) {
        return cntGiven[find(x)];
    }
    int sz(int x) {
        return -p[find(x)];
    }
    int edges(int x) {
        return cntEdges[find(x)];
    }
};

int n; int m; int k;

int f(int x, int y) {
    return x * m + y;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> m >> k;
        vector<array<int, 2>> xy(k + 1);
        bool ok = true;
        for(int i = 0; i < k + 1; i++) {
            cin >> xy[i][0] >> xy[i][1];
            xy[i][0]--; xy[i][1]--;
        }
        DSU dsu(n * m);
        for(int i = 0; i < k; i++) {
            dsu.setGiven(f(xy[i][0], xy[i][1]));
            if(abs(xy[i][0] - xy[i + 1][0]) + abs(xy[i][1] - xy[i + 1][1]) != 2) {
                ok = false;
                break;
            }
        }
        if(!ok) {
            cout << "0\n";
            continue;
        }
        for(int i = 0; i < k; i++) {
            if(abs(xy[i][0] - xy[i + 1][0]) == 1) {
                int dx = xy[i + 1][0] - xy[i][0];
                int dy = xy[i + 1][1] - xy[i][1];
                dsu.unite(f(xy[i][0], xy[i][1]), f(xy[i][0] + dx, xy[i][1]));
                dsu.unite(f(xy[i][0], xy[i][1]), f(xy[i][0], xy[i][1] + dy));
            } else {
                int dx = (xy[i + 1][0] - xy[i][0]) / 2;
                int dy = (xy[i + 1][1] - xy[i][1]) / 2;
                dsu.unite(f(xy[i][0], xy[i][1]), f(xy[i][0] + dx, xy[i][1] + dy));
            }
        }
        int wochien = 1;
        for(int i = 0; i < n; i++) {
            for(int j = 0; j < m; j++) {
                if(dsu.find(f(i, j)) == f(i, j) && dsu.given(f(i, j))) {
                    if(dsu.given(f(i, j)) < dsu.sz(f(i, j)) - dsu.given(f(i, j))) {
                        wochien = (1LL * wochien * (dsu.sz(f(i, j)) - dsu.given(f(i, j)))) % MOD;
                    } else if(dsu.given(f(i, j)) > dsu.sz(f(i, j)) - dsu.given(f(i, j))) {
                        wochien = 0;
                    } else if(dsu.edges(f(i, j)) == dsu.sz(f(i, j))) {
                        wochien = (wochien * 2) % MOD;
                    }
                }
            }
        }
        cout << wochien << "\n";
    }
}
