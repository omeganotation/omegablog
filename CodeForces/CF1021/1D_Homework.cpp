#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int MX = 1 << 20;

int n; 
string s; string t;

template<int K = 1> void f(int k) {
    if(k > K) {
        f<min(2 * K, MX)>(k);
        return;
    }
    vector<bitset<K>> sStrings(n / k);
    vector<bitset<K>> tStrings(n / k);
    for(int i = 0; i < n; i++) {
        sStrings[i / k][i % k] = s[i] - '0';
        tStrings[i / k][i % k] = t[i] - '0';
    }
    vector<bitset<K>> both = sStrings;
    for(bitset<K> i : tStrings) {
        both.push_back(i);
    }
    vector<bitset<K>> sBasis; vector<int> msbS;
    vector<bitset<K>> tBasis; vector<int> msbT;
    for(int i = 0; i < n / k; i++) {
        if(sStrings[i].any() && sBasis.size() < k) {
            for(int j = 0; j < sBasis.size(); j++) {
                if(sStrings[i][msbS[j]]) {
                    sStrings[i] ^= sBasis[j];
                    if(sStrings[i].none()) {
                        break;
                    }
                }
            }
            if(sStrings[i].any()) {
                int msb = -1;
                for(int j = 0; j < k; j++) {
                    if(sStrings[i][j]) {
                        msb = j;
                    }
                }
                sBasis.push_back(sStrings[i]);
                msbS.push_back(msb);
            }
        }
        if(tStrings[i].any() && tBasis.size() < k) {
            for(int j = 0; j < tBasis.size(); j++) {
                if(tStrings[i][msbT[j]]) {
                    tStrings[i] ^= tBasis[j];
                    if(tStrings[i].none()) {
                        break;
                    }
                }
            }
            if(tStrings[i].any()) {
                int msb = -1;
                for(int j = 0; j < k; j++) {
                    if(tStrings[i][j]) {
                        msb = j;
                    }
                }
                tBasis.push_back(tStrings[i]);
                msbT.push_back(msb);
            }
        }
    }
    if(sBasis.size() != tBasis.size()) {
        cout << "No\n";
        return;
    }
    sBasis.clear(); msbS.clear();
    for(int i = 0; i < both.size(); i++) {
        if(both[i].any() && sBasis.size() < k) {
            for(int j = 0; j < sBasis.size(); j++) {
                if(both[i][msbS[j]]) {
                    both[i] ^= sBasis[j];
                    if(both[i].none()) {
                        break;
                    }
                }
            }
            if(both[i].any()) {
                int msb = -1;
                for(int j = 0; j < k; j++) {
                    if(both[i][j]) {
                        msb = j;
                    }
                }
                sBasis.push_back(both[i]);
                msbS.push_back(msb);
                if(sBasis.size() > tBasis.size()) {
                    cout << "NO\n";
                    return;
                }
            }
        }
    }
    cout << "Yes\n";
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> s >> t;
        int k = n;
        while(!(k % 2)) {
            k /= 2;
        }
        f(k);
    }
}
