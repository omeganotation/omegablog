#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int exGCD(int a, int b, ll& x, ll& y) {
    if(!b) {
        x = 1;
        y = 0;
        return a;
    }
    ll x1; ll y1;
    int g = exGCD(b, a % b, x1, y1);
    x = y1;
    y = x1 - y1 * (a / b);
    return g;
}

ll modInverse(int a, int m) {
    ll x; ll y;
    int g = exGCD(a, m, x, y);
    return (x % m + m) % m;
}

vector<array<int, 2>> factor(int x) {
    vector<array<int, 2>> wochien;
    for(int i = 2; i * i <= x; i++) {
        if(!(x % i)) {
            int v = 1;
            while(!(x % i)) {
                v *= i;
                x /= i;
            }
            wochien.push_back({v, i});
        }
    }
    if(x) {
        wochien.push_back({x, x});
    }
    return wochien;
}

int n; int x; int y; int vx; int vy;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> x >> y >> vx >> vy;
        int g = __gcd(vx, vy);
        vx /= g; vy /= g;
        int needX = n - x; 
        int needY = n - y;
        g = __gcd(n, vx);
        if(g != 1 && needX % g) {
            cout << "-1\n";
            continue;
        }
        int conditionA = (1LL * (needX / g) * modInverse(vx / g, n / g)) % (n / g); int modA = n / g;
        g = __gcd(n, vy);
        if(g != 1 && needY % g) {
            cout << "-1\n";
            continue;
        }
        int conditionB = (1LL * (needY / g) * modInverse(vy / g, n / g)) % (n / g); int modB = n / g;
        g = __gcd(modA, modB);
        if(conditionA % g != conditionB % g) {
            cout << "-1\n";
            continue;
        }
        vector<array<int, 2>> primes = factor(g);
        for(array<int, 2> i : primes) {
            if(modA % (1LL * i[0] * i[1])) {
                conditionA = conditionA % (modA / i[0]);
                modA /= i[0];
            } else {
                conditionB = conditionB % (modB / i[0]);
                modB /= i[0];
            }
        }
        ll mFinal = 1LL * modA * modB;
        ll wochien = ((((__int128)((conditionA * modInverse(modB, modA)))) * modB) % mFinal) + ((((__int128)(conditionB * modInverse(modA, modB))) * modA) % mFinal);
        wochien %= mFinal;
        ll fx = (x + (__int128)wochien * vx) / n;
        ll fy = (y + (__int128)wochien * vy) / n;
        cout << fx + fy - 2 + (max(fy, fx) - min(fy, fx)) / 2 + (max(fy, fx) + min(fy, fx)) / 2 << "\n";
    }
}
