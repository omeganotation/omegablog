#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        sort(a.begin(), a.end());
        bool wochien = false;
        for(int i = 0; i < n; ) {
            int j = i;
            int len = 0;
            int cntLenTwo = 0;
            while(j < n && (i == j || a[j] - a[j - 1] <= 1)) {
                if(!len || a[j] == a[j - 1]) {
                    len++;
                } else {
                    len = 1;
                }
                if(len == 2) {
                    cntLenTwo++;
                }
                if(len == 4 || cntLenTwo == 2) {
                    wochien = true;
                    break;
                }
                j++;
            }
            if(wochien) {
                break;
            }
            i = j;
        }
        cout << (wochien ? "Yes" : "No") << "\n";
    }
}
