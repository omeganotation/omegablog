#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 998244353;
const int INF = 1E9; const ll INFLL = 1E18;

const int MAX = 501;
int ways[MAX][MAX + 1][2];

int n; int k;
string s;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    for(int i = 0; i < MAX; i++) {
        vector<vector<array<int, 2>>> dp(i + 1, vector<array<int, 2>>(i + 2, {0, 0}));
        for(int j = 0; j <= i; j++) {
            if(!j) {
                dp[0][0][0] = 1; dp[0][0][1] = 1; dp[0][1][0] = 1;
                continue;
            }
            for(int l = 0; l <= j + 1; l++) {
                dp[j][l][0] = dp[j - 1][l][0];
                dp[j][l][1] = dp[j - 1][l][1];
                if(l) {
                    if((j + l - 1) % 2) {
                        dp[j][l][1] += dp[j - 1][l - 1][1];
                        if(dp[j][l][1] >= MOD) {
                            dp[j][l][1] -= MOD;
                        }
                    } else {
                        dp[j][l][0] += dp[j - 1][l - 1][0];
                        if(dp[j][l][0] >= MOD) {
                            dp[j][l][0] -= MOD;
                        }
                    }
                } 
            } 
        }
        for(int j = 0; j <= i + 1; j++) {
            ways[i][j][0] = dp[i][j][0];
            ways[i][j][1] = dp[i][j][1];
        }
    }
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> k >> s;
        vector<vector<int>> dp(n + 1, vector<int>(k + 1));
        dp[n][0] = 1;
        for(int i = n - 1; i >= 0; i--) {
            for(int j = 0; j <= k; j++) {
                for(int l = 0; j + l <= k; l++) {
                    dp[i][j + l] += (1LL * dp[i + 1][j] * ways[j][l][s[i] - '0']) % MOD;
                    if(dp[i][j + l] >= MOD) {
                        dp[i][j + l] -= MOD;
                    }
                }
            }
        }
        cout << dp[0][k] << "\n";
    } 
}
