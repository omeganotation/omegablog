#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n; int m; int l;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> m >> l;
        vector<int> A(l);
        for(int i = 0; i < l; i++) {
            cin >> A[i];
        }
        vector<vector<int>> adj(n);
        for(int i = 0; i < m; i++) {
            int u; int v;
            cin >> u >> v;
            u--; v--;
            adj[u].push_back(v);
            adj[v].push_back(u);
        }
        vector<array<int, 2>> dist(n, {2 * INF + 10, 2 * INF + 10});
        queue<array<int, 2>> q;
        q.push({0, 0});
        dist[0][0] = 0;
        while(q.size()) {
            array<int, 2> x = q.front(); q.pop();
            for(int i : adj[x[0]]) {
                if(dist[i][x[1] ^ 1] > dist[x[0]][x[1]] + 1) {
                    dist[i][x[1] ^ 1] = dist[x[0]][x[1]] + 1;
                    q.push({i, x[1] ^ 1});
                }
            }
        }
        vector<int> odds;
        int sumEven = 0;
        int sumOdd = 0;
        for(int i : A) {
            if(i % 2) {
                odds.push_back(i);
                sumOdd += i;
            } else {
                sumEven += i;
            }
        }
        sort(odds.begin(), odds.end());
        int maxOdd = (!odds.size() ? -1 : (sumEven + (odds.size() % 2 ? sumOdd : sumOdd - odds[0])));
        int maxEven = sumEven + (odds.size() % 2 ? sumOdd - odds[0] : sumOdd);
        for(int i = 0; i < n; i++) {
            if(dist[i][0] <= maxEven || dist[i][1] <= maxOdd) {
                cout << '1';
            } else {
                cout << "0";
            }
        }
        cout << "\n"; 
    }
}
