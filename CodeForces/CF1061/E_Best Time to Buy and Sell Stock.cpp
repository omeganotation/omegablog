#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

vector<int> merge(vector<int>& possible, vector<int> a) {
    if (!possible.size()) {
        return possible;
    }

    vector<int> wochien;
    for (int i : a) {
        if (find(possible.begin(), possible.end(), i) != possible.end()) {
            wochien.push_back(i);
        }
    }
    return wochien;
}

bool f(int x, vector<int>& a) {
    vector<int> possible(n); 
    iota(possible.begin(), possible.end(), 0);

    set<array<int, 2>> left;
    set<array<int, 2>> right;
    for (int i = 0; i < n; i++) {
        right.insert({a[i], i});
    }

    for (int i = 0; i < n; i++) {
        right.erase({a[i], i});

        int goodLeft = 0;
        if (left.size() && (*left.begin())[0] + x <= a[i]) {
            goodLeft = 1;
            if (left.size() > 1 && (*next(left.begin()))[0] + x <= a[i]) {
                goodLeft = 2;
                if (left.size() > 2 && (*next(next(left.begin())))[0] + x <= a[i]) {
                    goodLeft = 3;
                }
            }
        }

        int goodRight = 0;
        if (right.size() && (*prev(right.end()))[0] - x >= a[i]) {
            goodRight = 1;
            if (right.size() > 1 && (*prev(prev(right.end())))[0] - x >= a[i]) {
                goodRight = 2;
                if (right.size() > 2 && (*prev(prev(prev(right.end()))))[0] - x >= a[i]) {
                    goodRight = 3;
                }
            }
        }


        if (goodLeft == 3) {
            possible = merge(possible, {i});
        } 
        else if (goodLeft == 2) {
            possible = merge(possible, {(*left.begin())[1], (*next(left.begin()))[1], i});
        }

        if (goodRight == 3) {
            possible = merge(possible, {i});
        }
        else if (goodRight == 2) {
            possible = merge(possible, {(*prev(right.end()))[1], (*prev(prev(right.end())))[1], i});
        }
        
        if (goodLeft >= 2 && goodRight >= 2) {
            possible = merge(possible, {i});
        }
        else if (goodLeft >= 2 && goodRight) {
            possible = merge(possible, {i, (*prev(right.end()))[1]});
        }
        else if (goodLeft && goodRight >= 2) {
            possible = merge(possible, {i, (*left.begin())[1]});
        }
        else if (goodLeft && goodRight) {
            possible = merge(possible, {i, (*left.begin())[1], (*prev(right.end()))[1]});
        }
        
        left.insert({a[i], i});
    }

    return !possible.size();
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    int T; cin >> T;
    for (int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; i++) {
            cin >> a[i];
        }

        int lo = -INF; int hi = INF; 
        while (lo < hi) {
            int mid = lo + (hi - lo + 1) / 2;
            if (f(mid, a)) {
                lo = mid;
            } else {
                hi = mid - 1;
            }
        }
        cout << lo << "\n";
    }
}
