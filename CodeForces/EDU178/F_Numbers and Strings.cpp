#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

vector<pair<int, string>> goodX;

int n;

void f(string& cur, bool change, bool zero) {
    if(cur.size() == 9) {
        if(change) {
            string s = to_string(stoi(cur)) + to_string(stoi(cur) + 1);
            sort(s.begin(), s.end());
            goodX.push_back(make_pair(stoi(cur), s));
        }
        return;
    }
    if(change) {
        cur += "9";
        f(cur, true, false);
        cur.pop_back();
    } else {
        for(int i = 0; i < 9; i++) {
            cur += to_string(i);
            f(cur, true, false);
            cur.pop_back();
        }
        if(zero || (count(cur.begin(), cur.end(), '0') == cur.size() - 1 && cur.back() != '0')) {
            cur += "0";
            f(cur, false, true);
            int firstNonzero = 0;
            for(int i = 0; i < cur.size(); i++) {
                if(cur[i] != '0') {
                    firstNonzero = cur[i] - '0';
                    break;
                }
            }
            cur.pop_back();    
            for(int i = firstNonzero; i < 10; i++) {
                cur += to_string(i);
                f(cur, false, false);
                cur.pop_back();
            }
        } else {
            for(int i = (cur.size() ? cur.back() - '0' : 0); i < 10; i++) {
                cur += to_string(i);
                f(cur, false, false);
                cur.pop_back();
            }
        }
    }
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    string cur = "";
    f(cur, false, false);
    vector<int> wochien;
    set<string> st;
    stable_sort(goodX.begin(), goodX.end());
    for(int i = 0; i < goodX.size(); i++) {
        if(!st.count(goodX[i].second)) {
            wochien.push_back(goodX[i].first);
            st.insert(goodX[i].second);
        }
    }
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        cout << upper_bound(wochien.begin(), wochien.end(), n) - wochien.begin() - 1 << "\n";
    }
}
