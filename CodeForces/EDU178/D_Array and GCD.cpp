#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int MAX = 1E7;
const int N = 4E5;
ll sumNeeded[N + 1];
vector<bool> isPrime(MAX, true);
basic_string<int> primes;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    isPrime[0] = false; isPrime[1] = false;
    for(int i = 2; i < MAX && primes.size() < N; i++) {
        if(isPrime[i]) {
            primes.push_back(i);
            for(int j = i + i; j < MAX; j += i) {
                isPrime[j] = false;
            }
        }
    }
    for(int i = 1; i <= N; i++) {
        sumNeeded[i] = sumNeeded[i - 1] + primes[i - 1];
    }
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        sort(a.begin(), a.end());
        ll sum = 0;
        for(int i : a) {
            sum += i;
        }
        for(int i = 0; i < n; i++) {
            if(sum >= sumNeeded[n - i]) {
                cout << i << "\n";
                break;
            }    
            sum -= a[i];
        }
    }
}
