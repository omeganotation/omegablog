#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n; int k; string s;
int q;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cin >> n >> k >> s >> q;
    vector<vector<int>> nxt(n, vector<int>(k, n));
    for(int i = n - 1; i >= 0; i--) {
        if(i + 1 < n) {
            for(int j = 0; j < k; j++) {
                nxt[i][j] = nxt[i + 1][j];
            }
        }
        nxt[i][s[i] - 'a'] = i;
    }
    vector<int> wochien(n + 1, INF);
    wochien[n] = 1;
    for(int i = n - 1; i >= 0; i--) {
        for(int j = 0; j < k; j++) {
            if(nxt[i][j] == n) {
                wochien[i] = 1;
            } else {
                wochien[i] = min(wochien[i], wochien[nxt[i][j] + 1] + 1);
            }
        }
    }
    for(int i = 0; i < q; i++) {
        string t; cin >> t;
        int cur = 0;
        bool ok = true;
        for(char j : t) {
            if(cur >= n || nxt[cur][j - 'a'] == n) {
                ok = false;
                break;
            }
            cur = nxt[cur][j - 'a'] + 1;
        }
        cout << (ok ? wochien[cur] : 0) << "\n";
    } 
}
