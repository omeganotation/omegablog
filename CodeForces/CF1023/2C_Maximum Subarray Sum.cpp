#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n; ll k;
string s;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> k >> s;
        vector<ll> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        if(count(s.begin(), s.end(), '1') == n) {
            ll wochien = 0;
            ll cur = 0;
            for(int i : a) {
                cur += i;
                wochien = max(wochien, cur);
                if(cur < 0) {
                    cur = 0;
                }
            }
            if(wochien == k) {
                cout << "Yes\n";
                for(int i : a) {
                    cout << i << " ";
                }
                cout << "\n";
            } else {
                cout << "No\n";
            }
            continue;
        }
        int zero = find(s.begin(), s.end(), '0') - s.begin();
        ll cur = 0;
        for(int i = 0; i < zero; i++) {
            cur += a[i];
            if(cur < 0) {
                cur = 0;
            }
        }
        int nextZero = find(s.begin() + zero + 1, s.end(), '0') - s.begin();
        ll cur2 = 0;
        for(int i = nextZero - 1; i > zero; i--) {
            cur2 += a[i];
            if(cur2 < 0) {
                cur2 = 0;
            }
        }
        a[zero] = k - cur - cur2;
        for(int i = zero + 1; i < n; i++) {
            if(s[i] == '0') {
                a[i] = -INFLL;
            }
        }
        ll wochien = 0;
        cur = 0;
        for(ll i : a) {
            cur += i;
            wochien = max(wochien, cur);
            if(cur < 0) {
                cur = 0;
            }
        }
        if(wochien == k) {
            cout << "Yes\n";
            for(ll i : a) {
                cout << i << " ";
            }
            cout << "\n";
        } else {
            cout << "No\n";
        }
    }
}
