#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int MAX = 1.5E5;
bool seen[MAX];
array<int, 2> dist[MAX];
vector<array<int, 3>> wochien;
array<int, 2> furthest;
int prv[MAX];

int n;

void dfs2(int x, vector<vector<int>>& adj, int p) {
    furthest = max(furthest, dist[x]);
    for(int i : adj[x]) {
        if(i != p && !seen[i]) {
            prv[i] = x;
            dist[i] = {dist[x][0] + 1, i};
            dfs2(i, adj, x);
        }
    }
}

void dfs(int x, vector<vector<int>>& adj) {
    dist[x] = {0, x};
    furthest = {-1, -1};
    dfs2(x, adj, -1);
    int u = furthest[1];
    dist[u] = {0, u};
    furthest = {-1, -1};
    prv[u] = -1;
    dfs2(u, adj, -1);
    int v = furthest[1];
    wochien.push_back({furthest[0] + 1, max(u, v) + 1, min(u, v) + 1});
    vector<int> path;
    while(v != -1) {
        path.push_back(v);
        v = prv[v];
    }
    for(int i : path) {
        seen[i] = true;
    }
    for(int i : path) {
        for(int j : adj[i]) {
            if(!seen[j]) {
                dfs(j, adj);
            }
        }
    }
} 

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<vector<int>> adj(n);
        for(int i = 0; i + 1 < n; i++) {
            int u; int v;
            cin >> u >> v;
            u--; v--;
            adj[u].push_back(v);
            adj[v].push_back(u);
            seen[i + 1] = false;
        }
        seen[0] = false;
        wochien.clear();
        dfs(0, adj);
        sort(wochien.begin(), wochien.end(), greater<array<int, 3>>());
        for(array<int, 3> i : wochien) {
            for(int j : i) {
                cout << j << " ";
            }
        }
        cout << "\n";
    }
}
