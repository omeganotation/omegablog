#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n; ll k;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> k;
        ll maxWeight = 1LL * n * (n - 1) * (n - 2) / 6;
        if(maxWeight + 1 < k) {
            cout << "No\n";
            continue;
        }
        int len = n;
        vector<int> extra(n);
        int lastExtra = 0;
        ll cur = maxWeight;
        while(abs(k - cur) > 1) {
            int space = len - lastExtra - 2;
            ll subtract = 1LL * space * (space + 1) / 2; 
            for(int i = lastExtra; i + 1 < len; i++) {
                ll newWeight = cur - subtract; 
                if(k <= newWeight + 1) {
                    extra[i]++;
                    lastExtra = i;
                    len--;
                    cur = newWeight;
                    break;
                }
                subtract -= (len - 2 - i);
            }
        }
        int totNodes = len;
        for(int i : extra) {
            totNodes += i;
        }
        extra[0] += n - totNodes;
        cout << "Yes\n";
        int node = len + 1;
        for(int i = 0; i + 1 < len; i++) {
            cout << i + 1 << " " << i + 2 << "\n";
            for(int j = 0; j < extra[i]; j++) {
                cout << i + 1 << " " << node++ << "\n";
            }
        }
    }
}
