#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int LOG = 30;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        int lsb = -1; int msb = -1;
        for(int i = 0; i < LOG; i++) {
            if(n & (1 << i)) {
                if(lsb == -1) {
                    lsb = i;
                }
                msb = i;
            }
        }
        string s = "";
        for(int i = lsb; i <= msb; i++) {
            s += to_string((n >> i) & 1);
        }
        string rev = s; reverse(rev.begin(), rev.end());
        bool ok = (s == rev);
        if(s.size() % 2 && s[s.size() / 2] == '1') {
            ok = false;
        }
        cout << (ok ? "YES" : "NO") << "\n";
    }
}
