#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n; int m;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> m;
        vector<string> G(n);
        for(int i = 0; i < n; i++) {
            cin >> G[i];
        }
        bool transposed = false;
        if(n > m) {
            vector<string> G2(m, string(n, '0'));
            for(int i = 0; i < n; i++) {
                for(int j = 0; j < m; j++) {
                    G2[j][i] = G[i][j];
                }
            }
            G = G2;
            swap(n, m);
            transposed = true;
        }
        vector<vector<vector<int>>> dp(m, vector<vector<int>>(n, vector<int>(n, INF)));
        for(int i = 0; i < n; i++) {
            for(int j = i + 1; j < n; j++) {
                int prv = -1;
                for(int k = 0; k < m; k++) {
                    if(G[i][k] == '1' && G[j][k] == '1') {
                        if(prv != -1) {
                            int area = (j - i + 1) * (k - prv + 1);
                            for(int l = prv; l <= k; l++) {
                                dp[l][i][j] = min(dp[l][i][j], area);
                            }
                        }
                        prv = k;
                    }
                }
            }
        }
        for(int i = 0; i < m; i++) {
            for(int j = n; j > 1; j--) {
                for(int k = 0; k + j - 1 < n; k++) {
                    dp[i][k + 1][k + j - 1] = min(dp[i][k + 1][k + j - 1], dp[i][k][k + j - 1]);
                    dp[i][k][k + j - 2] = min(dp[i][k][k + j - 2], dp[i][k][k + j - 1]);
                }
            }
        }
        vector<vector<int>> wochien(n, vector<int>(m));
        for(int i = 0; i < n; i++) {
            for(int j = 0; j < m; j++) {
                wochien[i][j] = (dp[j][i][i] == INF ? 0 : dp[j][i][i]);
            }
        }
        if(transposed) {
            vector<vector<int>> wochien2(m, vector<int>(n));
            for(int i = 0; i < n; i++) {
                for(int j = 0; j < m; j++) {
                    wochien2[j][i] = wochien[i][j];
                }
            }
            swap(n, m);
            wochien = wochien2;
        }
        for(int i = 0; i < n; i++) {
            for(int j = 0; j < m; j++) {
                cout << wochien[i][j] << " ";
            }
            cout << "\n";
        }
    }
}
