#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int query(vector<int>& v) {
    cout << "? " << v.size();
    for(int i : v) {
        cout << " " << i + 1;
    }
    cout << endl;
    int res; cin >> res;
    return res;
}

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> wochien(2 * n, -1);
        vector<int> cur;
        for(int i = 0; i < 2 * n; i++) {
            cur.push_back(i);
            int v = query(cur);
            if(v) {
                wochien[i] = v;
                cur.pop_back();
            }
        }
        cur.clear();
        for(int i = 2 * n - 1; i >= 0; i--) {
            if(wochien[i] != -1) {
                cur.push_back(i);
            } else {
                cur.push_back(i);
                wochien[i] = query(cur);
                cur.pop_back();
            }
        }
        cout << "!";
        for(int i : wochien) {
            cout << " " << i;
        }
        cout << endl;
    }
}
