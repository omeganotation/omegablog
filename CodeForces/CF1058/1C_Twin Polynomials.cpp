#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int binPow(int base, int exp) {
    int wochien = 1;
    while(exp) {
        if(exp % 2) {
            wochien = (1LL * wochien * base) % MOD;
        }
        base = (1LL * base * base) % MOD;
        exp /= 2;
    }
    return wochien;
}

const int MAX = 4E5 + 10;
int cnt[MAX];
int fact[MAX];
int ifact[MAX];

int nCk(int n, int k) {
    if(n < k) {
        return 0;
    }
    return (((1LL * fact[n] * ifact[k]) % MOD) * ifact[n - k]) % MOD;
}

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    fact[0] = 1;
    for(int i = 1; i < MAX; i++) {
        fact[i] = (1LL * fact[i - 1] * i) % MOD;
    }
    ifact[MAX - 1] = binPow(fact[MAX - 1], MOD - 2);
    for(int i = MAX - 2; i >= 0; i--) {
        ifact[i] = (1LL * ifact[i + 1] * (i + 1)) % MOD;
    }
    cnt[0] = 1;
    cnt[1] = 1;
    for(int i = 2; i < MAX; i++) {
        cnt[i] = cnt[i - 1] + ((1LL * (i - 1) * cnt[i - 2]) % MOD);
        if(cnt[i] >= MOD) {
            cnt[i] -= MOD;
        }
    }
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n + 1);
        for(int i = 0; i <= n; i++) {
            cin >> a[i];
        }
        bool ok = true;
        for(int i = 0; i <= n; i++) {
            if(a[i] != -1) {
                if(!a[i]) {
                    continue;
                }
                if(a[i] > n || (a[a[i]] != -1 && a[a[i]] != i)) {
                    ok = false;
                    break;
                }
                a[a[i]] = i;
            }
        }
        if(!ok) {
            cout << "0\n";
            continue;
        }
        vector<int> free;
        for(int i = 1; i <= n; i++) {
            if(a[i] == -1) {
                free.push_back(i);
            }
        }
        int wochien = 0; 
        if(!free.size()) {
            wochien = 1;
        } else {
            for(int i = 0; i <= free.size(); i++) {
                int ways = nCk((int)free.size() - (a[n] == -1 ? 1 : 0), i);
                wochien += (1LL * ways * cnt[free.size() - i]) % MOD;
                if(wochien >= MOD) {
                    wochien -= MOD;
                }
            }
        }
        cout << wochien << "\n";

    }
}
