#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int MAX = 4E5 + 10;
int lg[MAX];

double intersect(array<ll, 2> a, array<ll, 2> b) {
    double slopeA = 1.0 / a[0]; double slopeB = 1.0 / b[0];
    return (b[1] - a[1]) / (slopeA - slopeB);
}

double getY(array<ll, 2> a, ll v) {
    return a[1] + 1.0 * v / a[0];
}

int n; 

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    for(int i = 2; i < MAX; i++) {
        lg[i] = lg[i / 2] + 1;
    }
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<ll> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        vector<ll> mins;
        ll curMin = INFLL + 1;
        for(int i = n - 1; i >= 0; i--) {
            if(a[i] < curMin) {
                curMin = a[i];
                mins.push_back(a[i]);
            }
        }
        reverse(mins.begin(), mins.end());
        vector<ll> dp(mins.size() + 1);
        deque<array<ll, 2>> dq;
        dq.push_back({mins[0], 0});
        for(int i = 1; i <= mins.size(); i++) {
            while(dq.size() > 1 && getY(dq[0], mins[i - 1]) > getY(dq[1], mins[i - 1])) {
                dq.pop_front();
            }
            dp[i] = dq.front()[1] + (mins[i - 1] - 1) / dq.front()[0] + 1;           
            if(i < mins.size()) {
                array<ll, 2> line = {mins[i], dp[i]};
                while(dq.size() > 1 && intersect(dq.back(), line) < intersect(dq[dq.size() - 2], line)) {
                    dq.pop_back();
                } 
                dq.push_back(line);
            }
        }
        cout << dp[mins.size()] << "\n";
    }
}
