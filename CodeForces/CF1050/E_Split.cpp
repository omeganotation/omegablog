#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n; int k;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> k;
        vector<int> a(n);
        vector<int> cnt(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
            cnt[--a[i]]++;
        }
        bool ok = true;
        for(int i = 0; i < n; i++) {
            if(cnt[i] % k) {
                ok = false;
                break;
            }
        }
        if(!ok) {
            cout << "0\n";
            continue;
        }
        vector<int> cur(n);
        ll wochien = 0;
        for(int i = 0, j = -1; i < n; i++) {
            while(j + 1 < n && cur[a[j + 1]] + 1 <= cnt[a[j + 1]] / k) {
                cur[a[j + 1]]++;
                j++;
            }
            wochien += j - i + 1;
            cur[a[i]]--;
        }   
        cout << wochien << "\n";
    }
}
