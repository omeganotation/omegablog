#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        ll sumEven = 0;
        ll wochien = 0;
        vector<int> odds;
        for(int i : a) {
            if(!(i % 2)) {
                sumEven += i;
            } else {
                odds.push_back(i);
            }
            wochien += i;
        }
        if(!odds.size()) {
            cout << "0\n";
            continue;
        }
        sort(odds.begin(), odds.end());
        for(int i = 0; i < odds.size() / 2; i++) {
            wochien -= odds[i];
        }
        cout << wochien << "\n";
    }
}
