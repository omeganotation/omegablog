#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<vector<int>> a(n);
        int maxK = 0;
        for(int i = 0; i < n; i++) {
            int k; cin >> k;
            a[i].resize(k);
            maxK = max(maxK, k);
            for(int j = 0; j < k; j++) { 
                cin >> a[i][j]; 
            }
        }
        vector<vector<int>> aa(maxK);
        for(int i = 0; i < n; i++) {
            for(int j = 0; j < a[i].size(); j++) {
                aa[j].push_back(i);
            }
        }
        int cur = 0;
        vector<int> wochien;
        while(cur < maxK) {
            vector<int> possible = aa[cur];
            int cur2 = cur;
            while(possible.size() > 1 && cur2 < maxK) {
                vector<int> nextPossible;
                int minV = INF;
                for(int j : possible) {
                    if(cur2 == a[j].size()) {
                        possible = {j};
                    }
                    minV = min(minV, a[j][cur2]);
                }
                if(possible.size() == 1) {
                    break;
                }
                for(int j : possible) {
                    if(a[j][cur2] == minV) {
                        nextPossible.push_back(j);
                    }
                }
                possible = nextPossible; 
                cur2++;
            }
            if(!possible.size()) {
                break;
            }
            while(cur < a[possible[0]].size()) {
                wochien.push_back(a[possible[0]][cur++]);
            }
        }
        for(int i : wochien) {
            cout << i << " ";
        }
        cout << "\n";
    }
}
