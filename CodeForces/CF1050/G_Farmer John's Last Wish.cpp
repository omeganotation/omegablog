#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int MAX = 2E5 + 10;
basic_string<int> divs[MAX];

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    for(int i = 1; i < MAX; i++) {
        for(int j = i; j < MAX; j += i) {
            divs[j].push_back(i);
        }
    }
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        int wochien = 0;
        vector<int> cnt(n + 1);
        vector<int> maxes;
        for(int i = 0; i < n; i++) {
            vector<int> newMaxes;
            for(int j : divs[a[i]]) {
                cnt[j]++;
                if(cnt[j] < i + 1) {
                    wochien = max(wochien, cnt[j]);
                } else {
                    newMaxes.push_back(j);
                }
            }
            for(int j : maxes) {
                if(cnt[j] < i + 1) {
                    wochien = max(wochien, cnt[j]);
                }
            }
            maxes = newMaxes;
            cout << wochien << " ";
        }
        cout << "\n";
    } 
}
