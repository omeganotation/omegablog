#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int MAX = 2E5 + 10;
int pref[MAX];
int cnt[MAX];
ll value[MAX];
ll gains[MAX];

int n; int y;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> y;
        vector<int> c(n);
        for(int i = 0; i < n; i++) {
            cin >> c[i];
        }
        memset(pref, 0, sizeof(pref));
        memset(cnt, 0, sizeof(cnt));
        memset(value, 0, sizeof(value));
        for(int i : c) {
            pref[i]++;
            cnt[i]++;
        }
        for(int i = 1; i < MAX; i++) {
            pref[i] += pref[i - 1];
        }
        for(int i = 2; i < MAX; i++) {
            for(int j = 1; i * j + 1 < MAX; j++) {
                int l = min(MAX - 1, (i - 1) * (j + 1) + 1);
                int r = min(MAX - 1, i * (j + 1));
                value[j + 1] += 1LL * (min(cnt[i], pref[r] - pref[l - 1])) * y + 1LL * (pref[r] - pref[l - 1]) * i;
            }
        }
        ll wochien = -INFLL;
        for(int i = 2; i < MAX; i++) {
            ll add = 1LL * min(cnt[1], pref[i]) * y + pref[i];
            wochien = max(wochien, -1LL * y * n + value[i] + add);
        }
        cout << wochien << "\n";
    }
}
