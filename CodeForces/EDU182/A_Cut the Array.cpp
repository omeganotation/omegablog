#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        int sum = 0;
        for(int i : a) {
            sum += i;
        }
        int cur = 0;
        bool found = false;
        for(int i = 0; i < n; i++) {
            cur += a[i];
            int cur2 = 0;
            for(int j = i + 1; j + 1 < n; j++) {
                cur2 += a[j];
                int cur3 = sum - cur - cur2;
                if((cur % 3 != cur2 % 3 && cur % 3 != cur3 % 3 && cur2 % 3 != cur3 % 3) || (cur % 3 == cur2 % 3 && cur2 % 3 == cur3 % 3)) {
                    found = true;
                    cout << i + 1 << " " << j + 1 << "\n";
                    break;
                }
            }
            if(found) {
                break;
            }
        }
        if(!found) {
            cout << "0 0\n";
        }
    }
}
