#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const int MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> p(n);
        for(int i = 0; i < n; i++) {
            cin >> p[i]; p[i]--;
        }
        if(count(p.begin(), p.end(), -1) == 1) {
            vector<bool> missing(n, true);
            for(int i = 0; i < n; i++) {
                if(p[i] >= 0) {
                    missing[p[i]] = false;
                }
            }
            for(int i = 0; i < n; i++) {
                if(p[i] == -1) {
                    p[i] = find(missing.begin(), missing.end(), true) - missing.begin();
                }
            }
        }
        int l = 0; int r = n - 1;
        for(; l < n; l++) {
            if(p[l] != l) {
                break;
            }
        }
        for(; r >= 0; r--) {
            if(p[r] != r) {
                break;
            }
        }
        cout << (l <= r ? r - l + 1 : 0) << "\n";
    }
}
