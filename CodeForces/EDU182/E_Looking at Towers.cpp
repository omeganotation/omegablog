#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 998244353;
const int INF = 1E9; const ll INFLL = 1E18;

vector<int> order;

int get(int x) {
    return lower_bound(order.begin(), order.end(), x) - order.begin();
}

struct Seg {
    int n;
    vector<int> seg;
    vector<int> lazy;
    Seg() {}
    Seg(int n) {
        seg.resize(4 * n);
        lazy.resize(4 * n, 1);
    }
    void prop(int p) {
        if(lazy[p] != 1) {
            seg[p * 2] = (1LL * seg[p * 2] * lazy[p]) % MOD;
            lazy[p * 2] = (1LL * lazy[p * 2] * lazy[p]) % MOD;
            seg[p * 2 + 1] = (1LL * seg[p * 2 + 1] * lazy[p]) % MOD;
            lazy[p * 2 + 1] = (1LL * lazy[p * 2 + 1] * lazy[p]) % MOD;
            lazy[p] = 1;
        }
    }
    void update(int p, int l, int r, int i, int x) {
        if(l == r) {
            seg[p] = x;
            return;
        }
        prop(p);
        int mid = l + (r - l) / 2;
        if(i <= mid) {
            update(p * 2, l, mid, i, x);
        } else {
            update(p * 2 + 1, mid + 1, r, i, x);
        }
        seg[p] = seg[p * 2] + seg[p * 2 + 1];
        if(seg[p] >= MOD) {
            seg[p] -= MOD;
        }
    }
    void update2(int p, int l, int r, int i, int j, int x) {
        if(l == i && r == j) {
            seg[p] = (1LL * seg[p] * x) % MOD;
            lazy[p] = (1LL * lazy[p] * x) % MOD;
            return;
        }
        prop(p);
        int mid = l + (r - l) / 2;
        if(i <= mid) {
            update2(p * 2, l, mid, i, min(j, mid), x);
        }
        if(j > mid) {
            update2(p * 2 + 1, mid + 1, r, max(i, mid + 1), j, x);
        }
        seg[p] = seg[p * 2] + seg[p * 2 + 1];
        if(seg[p] >= MOD) {
            seg[p] -= MOD;
        }
    }
    int query(int p, int l, int r, int i, int j) {
        if(l == i && r == j) {
            return seg[p];
        }
        prop(p);
        int mid = l + (r - l) / 2;
        int wochien = 0;
        if(i <= mid) {
            wochien = query(p * 2, l, mid, i, min(j, mid));
        }
        if(j > mid) {
            wochien += query(p * 2 + 1, mid + 1, r, max(i, mid + 1), j);
            if(wochien >= MOD) {
                wochien -= MOD;
            }
        }
        return wochien;
    }
};

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        order.clear(); order.push_back(-1);
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        map<int, int> ind;
        int mx = -1;
        for(int i = 0; i < n; i++) {
            if(a[i] > mx) {
                mx = a[i];
                ind[a[i]] = order.size();
                order.push_back(a[i]);
            }
        }
        Seg seg(order.size());
        seg.update(1, 0, order.size() - 1, 0, 1);
        vector<int> dp(n);
        for(int i = 0; i < n; i++) {
            if(ind.count(a[i])) {
                if(a[i] < mx) {
                    int index = ind[a[i]];
                    int cur = 2 * seg.query(1, 0, order.size() - 1, index, index);
                    if(cur >= MOD) {
                        cur -= MOD;
                    }
                    cur += seg.query(1, 0, order.size() - 1, index - 1, index - 1);
                    if(cur >= MOD) {
                        cur -= MOD;
                    }
        
                    seg.update(1, 0, order.size() - 1, index, cur);
                    seg.update2(1, 0, order.size() - 1, get(a[i]) + 1, order.size() - 1, 2);
                }
            } else {
                seg.update2(1, 0, order.size() - 1, get(a[i]), order.size() - 1, 2);
            }
            dp[i] = seg.query(1, 0, order.size() - 1, order.size() - 2, order.size() - 2);
        }
        
        int wochien = 0;
        order.clear(); order.push_back(-1);
        ind.clear();
        mx = -1;
        for(int i = n - 1; i >= 0; i--) {
            if(a[i] > mx) {
                mx = a[i];
                ind[a[i]] = order.size();
                order.push_back(a[i]);
            }
        }
        seg = Seg(order.size());
        seg.update(1, 0, order.size() - 1, 0, 1);
        for(int i = n - 1; i >= 0; i--) {
            if(ind.count(a[i])) {
                int index = ind[a[i]];
                int cur = 2 * seg.query(1, 0, order.size() - 1, index, index);
                if(cur >= MOD) {
                    cur -= MOD;
                }
                cur += seg.query(1, 0, order.size() - 1, index - 1, index - 1);
                if(cur >= MOD) {
                    cur -= MOD;
                }
                int v = cur - seg.query(1, 0, order.size() - 1, index, index);;
                if(v < 0) {
                    v += MOD;
                }
                seg.update(1, 0, order.size() - 1, index, cur);
                if(index + 1 == order.size()) {
                    wochien += (1LL * v * (!i ? 1 : dp[i - 1])) % MOD;
                    if(wochien >= MOD) {
                        wochien -= MOD;
                    }
                }
                if(a[i] < mx) {
                    seg.update2(1, 0, order.size() - 1, get(a[i]) + 1, order.size() - 1, 2);
                }
            } else {
                seg.update2(1, 0, order.size() - 1, get(a[i]), order.size() - 1, 2);
            }
        }
        cout << wochien << "\n";

    }
}
