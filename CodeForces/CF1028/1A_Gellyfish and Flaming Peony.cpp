#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

const int MAX = 5000;
int gg[MAX + 1][MAX + 1];

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    for(int i = 0; i <= MAX; i++) {
        for(int j = 0; j <= MAX; j++) {
            if(!i) {
                gg[i][j] = j;
            } else {
                gg[i][j] = gg[j % i][i];
                gg[j][i] = gg[i][j];
            }
        }
    }
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        vector<int> dp(MAX + 1, INF);
        int g = a[0];
        for(int i : a) {
            dp[i] = 1;
            g = gg[g][i];
        }
        if(dp[g] == 1) {
            cout << n - count(a.begin(), a.end(), g) << "\n";
            continue;
        }
        for(int i = MAX; i >= g; i--) {
            if(dp[i] == INF) {
                continue;
            }
            for(int j : a) {
                int v = gg[i][j];
                dp[v] = min(dp[v], dp[i] + 1);
            }
        }
        cout << dp[g] + n - 2 << "\n";
    }
}
