#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 998244353;
const int INF = 1E9; const ll INFLL = 1E18;

const int MAX = 1E5;
int P[MAX];

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    P[0] = 1;
    for(int i = 1; i < MAX; i++) {
        P[i] = P[i - 1] * 2;
        if(P[i] >= MOD) {
            P[i] -= MOD;
        }
    }
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> p(n);
        vector<int> q(n);
        for(int i = 0; i < n; i++) {
            cin >> p[i];
        }
        for(int i = 0; i < n; i++) {
            cin >> q[i];
        }
        int maxQ = 0; int maxP = 0;
        vector<int> wochien(n);
        for(int i = 0; i < n; i++) {
            if(p[maxP] < p[i]) {
                maxP = i;
            }
            if(q[maxQ] < q[i]) {
                maxQ = i;
            }
            if(p[maxP] > q[maxQ]) {
                wochien[i] = P[p[maxP]] + P[q[i - maxP]];
                if(wochien[i] >= MOD) {
                    wochien[i] -= MOD;
                }
            } else if(p[maxP] < q[maxQ]) {
                wochien[i] = P[q[maxQ]] + P[p[i - maxQ]];
                if(wochien[i] >= MOD) {
                    wochien[i] -= MOD;
                }
            } else {
                wochien[i] = P[p[maxP]] + P[max(q[i - maxP], p[i - maxQ])];
                if(wochien[i] >= MOD) {
                    wochien[i] -= MOD;
                }
            }
        }
        for(int i : wochien) {
            cout << i << " ";
        }
        cout << "\n";
    }
}
