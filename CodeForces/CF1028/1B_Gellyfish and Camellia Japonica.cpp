#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n; int q;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n >> q;
        vector<int> b(n);
        for(int i = 0; i < n; i++) {
            cin >> b[i];
        }
        vector<int> bb = b;
        vector<array<int, 3>> xyz(q);
        for(int i = 0; i < q; i++) {
            cin >> xyz[i][0] >> xyz[i][1] >> xyz[i][2];
            xyz[i][0]--; xyz[i][1]--; xyz[i][2]--;
        }
        for(int i = q - 1; i >= 0; i--) {
            b[xyz[i][0]] = max(b[xyz[i][0]], b[xyz[i][2]]);
            b[xyz[i][1]] = max(b[xyz[i][1]], b[xyz[i][2]]);
            if(xyz[i][0] != xyz[i][2] && xyz[i][1] != xyz[i][2]) {
                b[xyz[i][2]] = 1;
            }
        }
        vector<int> c = b;
        for(int i = 0; i < q; i++) {
            c[xyz[i][2]] = min(c[xyz[i][0]], c[xyz[i][1]]);
        }
        if(c == bb) {
            for(int i : b) {
                cout << i << " ";
            }
            cout << "\n";
        } else {
            cout << "-1\n";
        }
    } 
}
