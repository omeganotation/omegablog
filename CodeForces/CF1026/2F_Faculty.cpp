#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<int> a(n);
        for(int i = 0; i < n; i++) {
            cin >> a[i];
        }
        int wochien = 0;
        int mx = -1;
        for(int i = 0; i < n; i++) {
            if(a[i] > mx) {
                if(mx * 2 > a[i]) {
                    wochien = a[i];
                } else {
                    int best = 0;
                    for(int j = 0; j < i; j++) {
                        best = max(best, a[j] + a[i] % a[j]);
                    }
                    wochien = best;
                }
                mx = a[i];
            } else if(a[i] < mx) {
                wochien = max(wochien, a[i] + mx % a[i]);
            }
            cout << wochien << " ";
        }
        cout << "\n";
    }
}
