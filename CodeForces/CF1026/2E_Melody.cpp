#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 1E9 + 7;
const int INF = 1E9; const ll INFLL = 1E18;

int n;

void dfs(int x, map<int, vector<array<int, 2>>>& adj, map<int, int>& lastEdge, vector<int>& eu, vector<bool>& seen, int prv) {
    while(lastEdge[x] < adj[x].size()) {
        int v = adj[x][lastEdge[x]][0];
        int e = adj[x][lastEdge[x]][1];
        lastEdge[x]++;
        if(seen[e]) {
            continue;
        }
        seen[e] = true;
        dfs(v, adj, lastEdge, eu, seen, e);
    }
    eu.push_back(prv);
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    int T; cin >> T;
    for(int test = 1; test <= T; test++) {
        cin >> n;
        vector<array<int, 2>> vp(n);
        for(int i = 0; i < n; i++) {
            cin >> vp[i][0] >> vp[i][1];
        }
        map<int, vector<array<int, 2>>> adj;
        for(int i = 0; i < n; i++) {
            adj[vp[i][0] * 2].push_back({vp[i][1] * 2 + 1, i});
            adj[vp[i][1] * 2 + 1].push_back({vp[i][0] * 2, i});
        }
        int odd = 0; int firstNode = vp[0][0] * 2;
        for(pair<int, vector<array<int, 2>>> i : adj) {
            if(i.second.size() % 2) {
                odd++;
                firstNode = i.first;
            }
        }
        if(odd != 2 && odd) {
            cout << "NO\n";
            continue;
        }
        vector<int> eu;
        map<int, int> lastEdge;
        vector<bool> seen(n);
        dfs(firstNode, adj, lastEdge, eu, seen, -1);
        if(eu.size() == n + 1) {
            cout << "YES\n";
            for(int i = 0; i < n; i++) {
                cout << eu[i] + 1 << " ";
            }
            cout << "\n";
        } else {
            cout << "NO\n";
        }
    } 
}
