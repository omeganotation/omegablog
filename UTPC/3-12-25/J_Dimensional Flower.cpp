#include <bits/stdc++.h>

using namespace std;

using ll = long long;
const ll MOD = 998244353;
const int INF = 1E9; const ll INFLL = 1E18;

const int MAX = 2E5 + 10; 
int fact[MAX];
int ifact[MAX];

int binPow(int base, int exp) {
    int wochien = 1;
    while(exp) {
        if(exp % 2) {
            wochien = (1LL * wochien * base) % MOD;
        }
        base = (1LL * base * base) % MOD;
        exp /= 2;
    }
    return wochien;
}

int nCk(int n, int k) {
    return (1LL * fact[n] * ((1LL * ifact[k] * ifact[n - k]) % MOD)) % MOD;
}

int d; int t;

int f(int n) {
    return (1LL * binPow(n, t) * binPow(2, n)) % MOD;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0); 
    fact[0] = 1;
    for(int i = 1; i < MAX; i++) {
        fact[i] = (1LL * fact[i - 1] * i) % MOD;
    }
    ifact[MAX - 1] = binPow(fact[MAX - 1], MOD - 2);
    for(int i = MAX - 2; i >= 0; i--) {
        ifact[i] = (1LL * ifact[i + 1] * (i + 1)) % MOD;
    }
    cin >> d >> t;
    int wochien = 0;
    for(int i = d; i >= 1; i--) {
        if(i % 2 == d % 2) {
            wochien += (1LL * nCk(d, i) * f(i)) % MOD;
            if(wochien >= MOD) {
                wochien -= MOD;
            }
        } else {
            wochien -= (1LL * nCk(d, i) * f(i)) % MOD;
            if(wochien < 0) {
                wochien += MOD;
            }
        }
    }
    cout << wochien << "\n";
}
